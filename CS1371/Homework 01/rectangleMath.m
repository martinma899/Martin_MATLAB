
function [area , perimeter] = rectangleMath (side1 , side2)

    % multiply the two sides to find the area
    area = side1 * side2;

    % use formula to find perimeter
    perimeter = (side1 + side2) * 2;

end

%{
Function Name: rectangleMath

Inputs: 1. (double) The width of a rectangle 2. (double) The height of a
rectangle

Outputs: 1. (double) The area of the rectangle 2. (double) The perimeter of
the rectangle

Function Description: Write a MATLAB function that takes in the width and
height of a rectangle, and outputs the area and perimeter of that
rectangle.
%}

